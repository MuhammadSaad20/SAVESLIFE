    <p style="color:gray; font-size:20px;">“If you're a blood donor, you're a hero to someone, somewhere, who received your gracious gift of life.”</p>
    
<div class="paragraph" style="color:gray; font-size:20px;">
<p>Blood bank:A place where blood is collected from donors, typed, separated into components</p>
<p>stored, and prepared for transfusion to recipients. A blood bank may be a separate free-standing</p>
<p>facility or part of a larger laboratory in a hospital.</p>   
<br> 
<p>Separation of blood: Typically, each donated unit of blood (whole blood) is separated into</p>
<p>multiple components, such as red blood cells, plasma and platelets. Each component is</p>
<p>generally transfused to a different individual, each with different needs.</p>
<br>
<p>An increasingly common blood bank procedure is apheresis, or the process of removing a</p>
<p>specific component of the blood, such as platelets, and returning the remaining components,</p>
<p>such as red blood cells and plasma, to the donor. This process allows more of one particular</p>
<p>part of the blood to be collected than could be separated from a unit of whole blood. Apheresis</p>
<p>is also performed to collect plasma (the liquid part of the blood) and granulocytes (white blood cells).</p>
<br>
<p>Who receives blood: Accident victims, people undergoing surgery and patients receiving</p>
<p>treatment for leukemia, cancer or other diseases, such as sickle cell disease and thalassemia,</p>
<p>all utilize blood. Over 20 million units of blood components are transfused every year in the US.</p>
<br>
<p>Other blood products: Other products derived from blood include albumin, immune globulin,</p>
<p>specific immune globulins and clotting factor concentrates. These blood products are</p>
<p>commonly made by commercial manufacturers.</p>
